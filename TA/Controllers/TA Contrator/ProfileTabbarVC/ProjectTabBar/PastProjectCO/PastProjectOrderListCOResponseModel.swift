import Foundation

// MARK: - PastProjectOrderListCOResponseModel
struct PastProjectOrderListCOResponseModel: Codable {
    let statusCode: Int?
    let message: String?
    let data: [PastPRojectOrderListCO]?
}

// MARK: - Datum
struct PastPRojectOrderListCO: Codable {
    let id, projectID, vendorID, contractorID: Int?
    let subtaskID, status, isContractor, isVendor: Int?
    let createdAt, updatedAt: String?
    let finalAmount, vendorAmount: Int?
    let addressLine1: String?
    let addressLine2: String?
    let zipCode, city, state, country: String?
    let isBlocked, isDeleted, totalItem, totalPrice: Int?
    let cartItem: [CartItem]?
    let vendorDetail, cartContractor: CartContractor?
//    let dispute: [JSONAny]

    enum CodingKeys: String, CodingKey {
        case id
        case projectID = "projectId"
        case vendorID = "vendorId"
        case contractorID = "contractorId"
        case subtaskID = "subtaskId"
        case status, isContractor, isVendor, createdAt, updatedAt, finalAmount, vendorAmount, addressLine1, zipCode, city, state, country, isBlocked, isDeleted, addressLine2
        case totalItem = "TotalItem"
        case totalPrice = "TotalPrice"
        case cartItem = "Cart_Item"
        case vendorDetail = "vendor_detail"
        case cartContractor = "cart_contractor"
//        case dispute
    }
}

// MARK: - CartContractor
struct CartContractor: Codable {
    let id: Int?
    let firstName, lastName, email: String?
}

// MARK: - CartItem
struct CartItem: Codable {
    let id, productID, cartID: Int?
    let item: String?
    let status, price, quantity, isDeleted: Int?
    let createdAt, updatedAt: String?
    let productsss: Productsss?

    enum CodingKeys: String, CodingKey {
        case id
        case productID = "productId"
        case cartID = "cartId"
        case item, status, price, quantity, isDeleted, createdAt, updatedAt, productsss
    }
}

// MARK: - Productsss
struct Productsss: Codable {
    let id: Int?
    let category: String?
//    let productFiles: [JSONAny]

    enum CodingKeys: String, CodingKey {
        case id, category
//        case productFiles = "product_files"
    }
}

